package Abc.LoopingStatement;

public class WhileLoop 
{
	public static void main(String[] args) 
	{
		//write no. 1to5
		int i=5;
		
		while(i>=0);
		{
			System.out.println(i);
			i--;     // after the printing statemement write increment 
		}
		System.out.println("end programm");
		
	}

}
