package Abc.StarPattern;

public class pattern5
{
public static void main(String[] args)
{
	for(int i=1;i<=4;i++)
	{
		System.out.println(" *");
	
		for(int j=1;j<=i;j++)
		{
			System.out.print(" ");
		}
		
	}
		for(int i=1;i<=5;i++)
		{
			System.out.println(" *");
			
			for(int j=3;j>=i;j--)
			{
				System.out.print (" ");
			}
			
		}
		
		
	}
	
	
}
	
	

	
	
	
	


